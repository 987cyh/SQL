SELECT * FROM NETFLIX n WHERE VIDEO_NAME LIKE '미%';
SELECT * FROM NETFLIX n WHERE VIDEO_NAME LIKE '%인';
SELECT * FROM NETFLIX n WHERE VIDEO_NAME LIKE '%의%';